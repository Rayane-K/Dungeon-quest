package Jeu;

public class Recuperation {
	private int x;
	private int y;
	private String image;
	
	public Recuperation(){
		this.x=0;
		this.y=0;
		this.image=new String();
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
	
	
}